static int char_to_font_index(char c) {
    if (c == ' ') return 0;
    if (c >= 'A' && c <= 'Z') return 1 + (c - 'A');
    if (c >= 'a' && c <= 'z') return 27 + (c - 'a');
    if (c >= '0' && c <= '9') return 53 + (c - '0');
    return 0; // fallback: space
}

void draw_char(struct limine_framebuffer *fb, int x, int y, char c, uint32_t color) {
    int font_index = char_to_font_index(c);
    const uint8_t* glyph = font8x8_basic[font_index];
    uint32_t *fb_ptr = (uint32_t *)fb->address;

    for (int row = 0; row < 8; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            bool pixel_on = bits & (1 << (7 - col));
            int px = x + col;
            int py = y + row;
            if (px >= 0 && px < (int)fb->width && py >= 0 && py < (int)fb->height) {
                fb_ptr[py * (fb->pitch / 4) + px] = pixel_on ? color : 0x000000;
            }
        }
    }
}

void draw_string(struct limine_framebuffer *fb, int x, int y, const char* str, uint32_t color) {
    while (*str) {
        draw_char(fb, x, y, *str, color);
        x += 8;
        str++;
    }
}
